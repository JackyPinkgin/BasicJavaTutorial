package controlflow;

public class BooleanExample {
    public static void main(String[] args){
        boolean value=true;
        if (value==true){
            System.out.println("it's true");
        }
        else {
            System.out.println("it's false");
        }

        if (value){
            System.out.println("it's true");
        }
        else {
            System.out.println("it's false");
        }
    }
}
